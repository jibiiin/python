n=int(input("enter 1st number"))
m=int(input("enter 2nd number"))
def sum(n,m):
    c=n+m
    return c
print(sum(n,m))
def multiplication(n,m):
    d=n*m
    return d
print(multiplication(n,m))  
def subtraction(n,m):
    e=n-m
    return e
print(subtraction(n,m))
def division(n,m):
    f=n//m
    return f
print(division(n,m))